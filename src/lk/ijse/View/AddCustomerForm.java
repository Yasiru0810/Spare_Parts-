    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String ID = IDText.getText();
        String Name = NameText.getText();
        String Address = AddressText.getText();
        String Contact = ContactText.getText();
        Customer customer = new Customer(ID,Name,Address,Contact);
        boolean isadded;
        try {
            isadded = new CustomerController().addCustomer(customer);
            if(isadded){
            JOptionPane.showMessageDialog(this, "Added Success");
            CustomerTableAdd();
        }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        String ID = IDText.getText();
        try {
            Customer customer=CustomerController.searchCustomer(ID);
        if(customer!=null){
          NameText.setText(customer.getName());
          AddressText.setText(customer.getAddress());
          ContactText.setText(customer.getContact());
        }else{
            JOptionPane.showMessageDialog(this, "Customer not found...");
        }
        } catch (ClassNotFoundException | SQLException ex) {
           JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
    }//GEN-LAST:event_SearchButtonActionPerformed

    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        try {
            String ID = IDText.getText();
            
            boolean isDeleted = CustomerController.deleteCustomer(ID);
            if (isDeleted) {
                JOptionPane.showMessageDialog(this, "Deleted..");
                CustomerTableAdd();
            } else {
                JOptionPane.showMessageDialog(this, "Delete fail..");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_DeleteButtonActionPerformed

