    DefaultTableModel modal;
    private void CustomerTableAdd() {
        try {
            ArrayList<Customer> suplist= new CustomerController().getAllCustomer();
            modal=(DefaultTableModel) TblCus.getModel();
            modal.setRowCount(0);
            for(Customer tempemp:suplist){
                
                modal.addRow(new Object[]{
                    tempemp.getID(),
                    tempemp.getName(),
                    tempemp.getAddress(),
                    tempemp.getContact(),
                    
                    
                });
            } 
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
}
